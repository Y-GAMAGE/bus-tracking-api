const express = require('express');
const router = express.Router();
const authController = require('../controllers/authController');
const { protect } = require('../middleware/auth');
const { 
  validateFields, 
  validateEmail, 
  validatePassword 
} = require('../middleware/validation');

/**
 * @swagger
 * tags:
 *   name: Authentication
 *   description: User authentication and authorization
 */

/**
 * @swagger
 * /api/auth/register:
 *   post:
 *     tags: [Authentication]
 *     summary: Register a new user
 *     description: Create a new user account (admin, operator, driver, or passenger)
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - username
 *               - email
 *               - password
 *             properties:
 *               username:
 *                 type: string
 *                 example: admin_ntc
 *               email:
 *                 type: string
 *                 format: email
 *                 example: admin@ntc.lk
 *               password:
 *                 type: string
 *                 minLength: 6
 *                 example: admin123
 *               role:
 *                 type: string
 *                 enum: [admin, operator, driver, passenger]
 *                 default: passenger
 *                 example: admin
 *               phone:
 *                 type: string
 *                 pattern: '^[0-9]{10}$'
 *                 example: "0771234567"
 *               profile:
 *                 type: object
 *                 properties:
 *                   firstName:
 *                     type: string
 *                     example: System
 *                   lastName:
 *                     type: string
 *                     example: Administrator
 *     responses:
 *       201:
 *         description: User registered successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 success:
 *                   type: boolean
 *                   example: true
 *                 message:
 *                   type: string
 *                   example: User registered successfully
 *                 data:
 *                   type: object
 *                   properties:
 *                     user:
 *                       $ref: '#/components/schemas/User'
 *                     token:
 *                       type: string
 *                       example: eyJhbGciOiJIUzI1NiIsInR5cCI6IkpXVCJ9...
 *       400:
 *         description: Validation error or user already exists
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */
router.post(
  '/register',
  validateFields(['username', 'email', 'password']),
  validateEmail,
  validatePassword,
  authController.register
);

